	//CAM_RST0
	PINMUX_CONFIG(PAD_MIPI_TXP2, XGPIOC_17);
	PINMUX_CONFIG(PAD_MIPI_TXM2, XGPIOC_16);
	//CAM_MCLK0
	PINMUX_CONFIG(PAD_MIPI_TXP0, CAM_MCLK0);
	PINMUX_CONFIG(PAD_MIPI_TXM0, XGPIOC_12);
	//IRCUT
